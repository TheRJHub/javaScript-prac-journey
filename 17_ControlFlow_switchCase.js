const month="dec";
switch (month) {
    case "jan":
        console.log(1);
        break;
    case "feb":
        console.log(2);
        break;
    case "mar":
        console.log(3);
        break;
    case "apr":
        console.log(4);
        break;

    case "nov":
        console.log(11);
        break;
    case "dec":
        console.log(12);
        break;
    case "sep":
        console.log(9);
        break;
    default:
        break;
}